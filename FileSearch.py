# dependencies
# -------------------------------------------------------------------------------------------------
from os import walk
from os import sep
from re import match
# -------------------------------------------------------------------------------------------------
# File Search
# -------------------------------------------------------------------------------------------------
class FileSearch:
	# -------------------------------------------------------------------------
	# find files
	# -------------------------------------------------------------------------
	@staticmethod
	def find(path, pattern):
		result = []
		for root, f, files in walk(path):
			for f in [root + sep + f for f in files if match(pattern, f)]:
				result.append(f)
			#
		#
		return result
	#
# -------------------------------------------------------------------------------------------------
# end
